using System;

/// <summary>
/// 水车组件类型枚举
/// </summary>
public enum WaterwheelComponent
{
    WaterwheelAxle,          // 水车中轴
    MainRib,                // 主辐条
    Link,                   // 连杆（包括内外部长短连杆）
	HorizontalCrossbar,     // 横杠
	Paddle,                 // 刮板
	WaterwheelSupport,      // 水车支架
	InclinedBambooTube,     // 斜装竹筒
	WaterChute              // 水舰（导水槽）
}

